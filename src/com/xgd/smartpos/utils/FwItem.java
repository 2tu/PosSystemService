
package com.xgd.smartpos.utils;

public class FwItem { 

	private String name;
	private String version;
	private String desc;
	private String emd5;
	private String updateDownAction;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getEmd5() {
		return emd5;
	}

	public void setEmd5(String emd5) {
		this.emd5 = emd5;
	}

	public String getUpdateDownAction() {
		return updateDownAction;
	}

	public void setUpdateDownAction(String updateDownAction) {
		this.updateDownAction = updateDownAction;
	}
}
